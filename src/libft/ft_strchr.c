char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	c1;

	i = 0;
	c1 = (char)c;
	while (s[i] != c1 && s[i])
		++i;
	if (s[i] == c1)
		return ((char *)(s + i));
	return (0);
}
